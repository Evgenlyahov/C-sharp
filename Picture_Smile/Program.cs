void PrintTwoDimArray(int[,] twoDimArray)
{
    for (int i = 0; i < twoDimArray.GetLength(0); i++)
    {
        for (int j = 0; j < twoDimArray.GetLength(1); j++)
        {
            if(twoDimArray[i, j] == 0) System.Console.Write(" ");
            if(twoDimArray[i, j] == 1) System.Console.Write("#");
            if(twoDimArray[i, j] == 2) System.Console.Write("$");
            if(twoDimArray[i, j] == 3) System.Console.Write(".");
        }
        System.Console.WriteLine();
    }
}

void FillSmile(int row, int columns)
{
    if (smileArray[row, columns] == 0)
    {
        smileArray[row, columns] = 3;
        FillSmile(row - 1, columns);
        FillSmile(row, columns - 1);
        FillSmile(row + 1, columns);
        FillSmile(row, columns + 1);
    }
}

